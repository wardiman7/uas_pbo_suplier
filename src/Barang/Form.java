    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
            int baris = tabelBarang.getSelectedRow();
            int id = Integer.parseInt(tabelBarang.getValueAt(baris, 0).toString());
            
            if(baris < 0){
                JOptionPane.showMessageDialog(this, "anda belum memilih data yang akan dihapus");
            } else {
                service.delete(id);
                reset();
                JOptionPane.showMessageDialog(this, "hapus data berhasil");
            }
    }//GEN-LAST:event_btnHapusActionPerformed

