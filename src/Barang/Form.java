    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        String idBarang = txtId.getText();
        String namaBarang = txtNama.getText();
        String harga = txtHarga.getText();
        String stok = txtQty.getText();

        if (idBarang.equals("") || namaBarang.equals("") || harga.equals("") || stok.equals("")) {
            JOptionPane.showMessageDialog(this, "inputan masih kosong");
        }else{
            Barang barang = new Barang ();
            
            barang.setIdBarang(idBarang);
            barang.setNamaBarang(namaBarang);
            barang.setHarga(Integer.parseInt(harga));
            barang.setStok(Integer.parseInt(stok));
            
            JOptionPane.showMessageDialog(this, "data berhasil disimpan");

            service.create(barang);
            reset();
        }
    }//GEN-LAST:event_btnTambahActionPerformed

